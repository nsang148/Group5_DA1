package ViewModels;

/**
 *
 * @author acer
 */
public class BaoCaoMail {
    private String ngay;
    private String ten;
    private int soLuong;
    private Double tongTien;

    public BaoCaoMail() {
    }

    public BaoCaoMail(String ngay, String ten, int soLuong, Double tongTien) {
        this.ngay = ngay;
        this.ten = ten;
        this.soLuong = soLuong;
        this.tongTien = tongTien;
    }

    public String getNgay() {
        return ngay;
    }

    public void setNgay(String ngay) {
        this.ngay = ngay;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }

    public Double getTongTien() {
        return tongTien;
    }

    public void setTongTien(Double tongTien) {
        this.tongTien = tongTien;
    }
    
}
